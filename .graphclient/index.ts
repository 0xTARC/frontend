const baseDir = pathModule.join(typeof __dirname === 'string' ? __dirname : '/', '..');

const importFn: ImportFn = <T>(moduleId: string) => {
  const relativeModuleId = (pathModule.isAbsolute(moduleId) ? pathModule.relative(baseDir, moduleId) : moduleId).split('\\').join('/').replace(baseDir + '/', '');
  switch(relativeModuleId) {
    case ".graphclient/sources/dyad-goerli/introspectionSchema":
      return import("./sources/dyad-goerli/introspectionSchema") as T;
    
    default:
      return Promise.reject(new Error(`Cannot find module '${relativeModuleId}'.`));
  }
};

const rootStore = new MeshStore('.graphclient', new FsStoreStorageAdapter({
  cwd: baseDir,
  importFn,
  fileType: "ts",
}), {
  readonly: true,
  validate: false
});

export const rawServeConfig: YamlConfig.Config['serve'] = undefined as any
export async function getMeshOptions(): Promise<GetMeshOptions> {
const pubsub = new PubSub();
const sourcesStore = rootStore.child('sources');
const logger = new DefaultLogger("GraphClient");
const cache = new (MeshCache as any)({
      ...({} as any),
      importFn,
      store: rootStore.child('cache'),
      pubsub,
      logger,
    } as any)

const sources: MeshResolvedSource[] = [];
const transforms: MeshTransform[] = [];
const additionalEnvelopPlugins: MeshPlugin<any>[] = [];
const dyadGoerliTransforms = [];
const additionalTypeDefs = [] as any[];
const dyadGoerliHandler = new GraphqlHandler({
              name: "dyad-goerli",
              config: {"endpoint":"https://api.thegraph.com/subgraphs/name/0xtarc/dyad-subgraph-goerli"},
              baseDir,
              cache,
              pubsub,
              store: sourcesStore.child("dyad-goerli"),
              logger: logger.child("dyad-goerli"),
              importFn,
            });
sources[0] = {
          name: 'dyad-goerli',
          handler: dyadGoerliHandler,
          transforms: dyadGoerliTransforms
        }
const additionalResolvers = [] as any[]
const merger = new(BareMerger as any)({
        cache,
        pubsub,
        logger: logger.child('bareMerger'),
        store: rootStore.child('bareMerger')
      })

  return {
    sources,
    transforms,
    additionalTypeDefs,
    additionalResolvers,
    cache,
    pubsub,
    merger,
    logger,
    additionalEnvelopPlugins,
    get documents() {
      return [
      {
        document: Top100NfTsDocument,
        get rawSDL() {
          return printWithCache(Top100NfTsDocument);
        },
        location: 'Top100NfTsDocument.graphql'
      }
    ];
    },
    fetchFn,
  };
}

